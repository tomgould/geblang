// NativeParamNames returns a native function's catalogued parameter names (the last token of each "type name" param string).
// paramName extracts the name from a "type name [= default]" catalog param (token before "=", else the last token; the type itself may contain spaces like "dict<string, any>").
func paramName(param string) string {
	fields := strings.Fields(param)
	if len(fields) == 0 {
		return ""
	}
	for i, f := range fields {
		if f == "=" && i > 0 {
			return fields[i-1]
		}
	}
	return fields[len(fields)-1]
}

func nativeParams(module, function string) ([]string, bool) {
	mod, ok := StdlibCatalog[module]
	if !ok {
		return nil, false
	}
	doc, ok := mod.Functions[function]
	if !ok {
		if cls, method, split := SplitQualifiedClass(function); split && mod.ClassMethods != nil {
			if methods, has := mod.ClassMethods[cls]; has {
				if d, hit := methods[method]; hit {
					doc, ok = d, true
				}
			}
		}
	}
	if !ok {
		return nil, false
	}
	return doc.Params, true
}

func NativeParamNames(module, function string) ([]string, bool) {
	params, ok := nativeParams(module, function)
	if !ok {
		return nil, false
	}
	names := make([]string, 0, len(params))
	for _, p := range params {
		if name := paramName(p); name != "" {
			names = append(names, name)
		}
	}
	return names, true
}

// NativeSignature builds the argument-binding signature (param names, defaults, variadic) for a native module function.
func NativeSignature(module, function string) (argbinding.Signature, bool) {
	params, ok := nativeParams(module, function)
	if !ok {
		return argbinding.Signature{}, false
	}
	sig := argbinding.Signature{FuncName: function}
	for i, p := range params {
		sig.ParamNames = append(sig.ParamNames, paramName(p))
		sig.HasDefault = append(sig.HasDefault, strings.Contains(p, " = "))
		if i == len(params)-1 && strings.HasPrefix(strings.TrimSpace(p), "...") {
			sig.Variadic = true
		}
	}
	return sig, true
}
